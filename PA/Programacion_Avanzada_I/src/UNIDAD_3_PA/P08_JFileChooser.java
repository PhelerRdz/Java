    private void Button01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button01ActionPerformed
        String query = TextField01.getText();
        boolean found = false;
        int x = 0;
        int y;
        if (!query.isEmpty()) {
            for (int i = 0; i < this.count; i++) {
                if (RadioButton01.isSelected()) {
                    if (stds[i].getMatricula().compareTo(query) == 0) {
                        found = true;
                        y = x + stds[i].getLong();
                        TextArea01.requestFocus();
                        TextArea01.select(x, y);
                        TextField02.setText(stds[i].getNombre());
                        TextField03.setText(stds[i].getApaterno());
                        TextField04.setText(stds[i].getAmaterno());
                        TextField05.setText(stds[i].getCarrera());
                        TextField06.setText(stds[i].getSemestre());
                        Button02.setEnabled(true);
                        break;
                    }
                    x += stds[i].getLong() + 1;
                }
            }
            if (!found) {
                String title = "Error";
                String message = "ID not found. Try again.";
                TextField01.setText("");
                TextField01.requestFocus();
                JOptionPane.showMessageDialog(this, message, title, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Button01ActionPerformed

