    private void MenuItem02ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuItem02ActionPerformed
        path = new File(currDir);
        FileChooser.setCurrentDirectory(path);
        
        int optionSelected = FileChooser.showOpenDialog(this);
//        System.out.println("Option Selected: " + optionSelected);
        if (optionSelected == JFileChooser.APPROVE_OPTION) {
            BufferedReader br = null;
            FileReader fr = null;
            try {
                f = FileChooser.getSelectedFile();
                if (f.isFile() && f.canRead()) {
                    TextArea01.selectAll();
                    TextArea01.replaceSelection("");

                    count = 0;
                    String nLine;
                    P08_Estudiante st;
                    stds = new P08_Estudiante[30];

                    fr = new FileReader(f);
                    br = new BufferedReader(fr);

                    while ((nLine = br.readLine()) != null) {
                        TextArea01.append(nLine);
                        TextArea01.append("\n");

                        step = nLine.length();
                        String[] tokens = nLine.split(" ");
                        st = new P08_Estudiante();
                        st.setMatricula(tokens[0]);
                        st.setApaterno(tokens[1]);
                        st.setAmaterno(tokens[2]);
                        st.setNombre(tokens[3]);
                        st.setCarrera(tokens[4]);
                        st.setSemestre(tokens[5]);
                        st.setLong(step);
                        stds[count] = st;
                        count++;
                    }

                    br.close();
                    TextField01.setEnabled(true);
                    RadioButton01.setEnabled(true);
                    RadioButton02.setEnabled(true);
                    Button01.setEnabled(true);

                    CheckBox01.setEnabled(true);
                    CheckBox02.setEnabled(true);
                    Button03.setEnabled(true);
                    if (f.canRead()) {
                        CheckBox01.setSelected(true);
                    }
                    if (f.canWrite()) {
                        CheckBox02.setSelected(true);
                    }
                }
            } catch (IOException ex) {
                System.out.println(ex);
            } finally {
                try {
                    if (fr != null) {
                        fr.close();
                    }
                } catch (IOException ex) {
                    System.out.println(ex);
                }
            }
        }
    }//GEN-LAST:event_MenuItem02ActionPerformed

