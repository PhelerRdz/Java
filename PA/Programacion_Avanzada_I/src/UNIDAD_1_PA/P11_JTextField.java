    private boolean allFilled() {
        boolean flag = true;
        if (TextField01.getText().isEmpty()
                || TextField02.getText().isEmpty()
                || TextField03.getText().isEmpty()
                || TextField04.getText().isEmpty()) {
            flag = false;
        }
        return flag;
    }

    private void clearComp() {
        TextField01.setText("");
        TextField02.setText("");
        TextField03.setText("");
        TextField04.setText("");
        boolean selected = ToggleButton01.isSelected();
        if (!selected) {
            ToggleButton01.setText("YES");
            ToggleButton01.setSelected(true);
        }
    }
