    private void Button01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button01ActionPerformed
        if (!allFilled()) {
            JOptionPane.showMessageDialog(this, "Please, fill all the blanks.", "Warning", JOptionPane.INFORMATION_MESSAGE);
        } else {            
            String name = TextField01.getText();
            String last_name = TextField02.getText();
            String phone = TextField03.getText();
            String email = TextField04.getText();
            boolean selected = ToggleButton01.isSelected();
            USER = new P11_User(name, last_name, phone, email, selected);
            String message = USER.toString();
            JOptionPane.showMessageDialog(this, message, "User Data", JOptionPane.INFORMATION_MESSAGE);
            clearComp();
        }
    }//GEN-LAST:event_Button01ActionPerformed

